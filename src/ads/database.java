    private void enterbtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_enterbtnMouseClicked
            String idText = empnum.getText().trim();

            if (idText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter Employee ID!");
                return;
            }

            try {
                int employeeId = Integer.parseInt(idText);

                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");

                String url = "jdbc:sqlserver://localhost:1433;" +
                        
                        "databaseName=attendance_sys;" +
                        "user=sa;" +
                        "password=sean;" +
                        "encrypt=true;" +
                        "trustServerCertificate=true;";

                try (Connection con = DriverManager.getConnection(url)) {

                    //Check if existing employee
                    String empSql = "SELECT first_name, last_name FROM Employees WHERE employee_id = ?";
                    PreparedStatement empPst = con.prepareStatement(empSql);
                    empPst.setInt(1, employeeId);

                    ResultSet empRs = empPst.executeQuery();

                    if (!empRs.next()) {
                        JOptionPane.showMessageDialog(this, "Employee not found!");
                        return;
                    }

                    String fullName = empRs.getString("first_name") + " " +
                          empRs.getString("last_name");


                    //Check attendance today
                    String checkSql = """
                        SELECT attendance_id
                        FROM Attendance
                        WHERE employee_id = ?
                        AND CAST(time_in AS DATE) = CAST(GETDATE() AS DATE)
                        AND time_out IS NULL
                    """;

                    PreparedStatement checkPst = con.prepareStatement(checkSql);
                    checkPst.setInt(1, employeeId);
                    ResultSet rs = checkPst.executeQuery();

                   
                    java.util.Date now = new java.util.Date();
                    java.text.SimpleDateFormat sdf =
                            new java.text.SimpleDateFormat("yyyy-MM-dd hh:mm:ss a");
                    String currentTime = sdf.format(now);

                    if (rs.next()) {
            //TIME OUT
            int attendanceId = rs.getInt("attendance_id");

            String outSql = "UPDATE Attendance SET time_out = ? WHERE attendance_id = ?";
            PreparedStatement outPst = con.prepareStatement(outSql);
            outPst.setString(1, currentTime);
            outPst.setInt(2, attendanceId);
            outPst.executeUpdate();

            loadAttendance();

            //DISPLAY TO PANEL
            lblStatus.setText("TIME OUT");
            lblName.setText("Name: " + fullName);
            lblTime.setText("Time: " + currentTime);

        } else {
            //TIME IN
            String inSql = """
            INSERT INTO Attendance (employee_id, shift_id, time_in)
            SELECT ?, shift_id, ?
            FROM Employees
            WHERE employee_id = ?
        """;

            PreparedStatement inPst = con.prepareStatement(inSql);
            inPst.setInt(1, employeeId);
            inPst.setString(2, currentTime);
            inPst.setInt(3, employeeId);
            inPst.executeUpdate();

            loadAttendance();

            //DISPLAY TO PANEL
            lblStatus.setText("TIME IN");
            lblName.setText("Name: " + fullName);
            lblTime.setText("Time: " + currentTime);

        }

                    empnum.setText("");
                }

            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid ID format!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
    }//GEN-LAST:event_enterbtnMouseClicked

