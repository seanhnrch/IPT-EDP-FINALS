    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
    
                 if (update_fname.getText().trim().isEmpty() ||
                 update_lname.getText().trim().isEmpty() ||
                 update_email.getText().trim().isEmpty() ||
                 update_position.getSelectedItem() == null ||
                 update_dept.getSelectedItem() == null ||
                 update_shift.getSelectedItem() == null) {

                 JOptionPane.showMessageDialog(this,
                     "All fields are required. Please fill in everything."
                     );
                 return; // stop here
             }
                 
    String first_name = update_fname.getText();
    String last_name  = update_lname.getText();
    String email      = update_email.getText();
    String position   = update_position.getSelectedItem().toString();

    // Split to get only the ID number from "3 - Teller Services"
    int department_id = Integer.parseInt(update_dept.getSelectedItem().toString().split(" - ")[0].trim());
    int shift_id      = Integer.parseInt(update_shift.getSelectedItem().toString().split(" - ")[0].trim());

    try {
        Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
        String url = "jdbc:sqlserver://localhost:1433;" +
                     "databaseName=attendance_sys;" +
                     "user=sa;" +
                     "password=sean;" +
                     "encrypt=true;" +
                     "trustServerCertificate=true;";

        Connection con = DriverManager.getConnection(url);

        String updateQuery = "UPDATE Employees SET first_name=?, last_name=?, position=?, department_id=?, email=?, shift_id=? WHERE employee_id=?";

        PreparedStatement ps = con.prepareStatement(updateQuery);
        ps.setString(1, first_name);
        ps.setString(2, last_name);
        ps.setString(3, position);
        ps.setInt(4, department_id);
        ps.setString(5, email);
        ps.setInt(6, shift_id);
        ps.setString(7, employee_id);

        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Update Successfully");
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }

    }//GEN-LAST:event_savebtnActionPerformed

