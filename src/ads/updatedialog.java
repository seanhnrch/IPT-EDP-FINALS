    private void loadDepartments() {
    try {
        String url = "jdbc:sqlserver://localhost:1433;"
                   + "databaseName=attendance_sys;"
                   + "encrypt=true;"
                   + "trustServerCertificate=true;";

        Connection con = DriverManager.getConnection(url, "sa", "sean");

        String sql = "SELECT departmen"
                + "t_id, department_name FROM Departments";
        PreparedStatement pst = con.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();

        update_dept.removeAllItems();

        while (rs.next()) {
            update_dept.addItem(
                rs.getInt("department_id") + " - " +
                rs.getString("department_name")
            );
        }
          update_dept.setSelectedIndex(-1);
        rs.close();
        pst.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
}
    
